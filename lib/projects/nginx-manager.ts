/**
 * lib/projects/nginx-manager.ts
 *
 * Safe Nginx config generation and domain publishing for Prisom projects.
 *
 * Route modes:
 *   fullstack_node  — all routes proxy to backend (default)
 *   static_plus_api — nginx serves static root, <apiPrefix> proxies to backend
 *   static_only     — nginx serves static files only (no backend)
 *   api_only        — all routes proxy to backend (same as fullstack_node)
 *
 * Security rules:
 *   - Domain names are validated against a strict regex before use
 *   - Config written with fs.writeFile (no shell)
 *   - Symlink created with fs.symlink (no shell)
 *   - nginx -t, systemctl reload, certbot run with execFile (no shell)
 *   - Port must come from the DB (validated to be in range 4100–4999)
 *   - Never runs as root — returns clear permission error messages
 *
 * Nginx layout:
 *   /etc/nginx/sites-available/<domain>.conf
 *   /etc/nginx/sites-enabled/<domain>.conf  (symlink)
 */

import path from "path";
import { promises as fs } from "fs";
import { execFile } from "child_process";
import { promisify } from "util";

const execFileAsync = promisify(execFile);

// ── Constants ──────────────────────────────────────────────────────────────

const NGINX_SITES_AVAILABLE = "/etc/nginx/sites-available";
const NGINX_SITES_ENABLED   = "/etc/nginx/sites-enabled";

const RESERVED_HOSTNAMES = new Set([
  "projects.doorstepmanchester.uk",
  "doorstepmanchester.uk",
  "www.doorstepmanchester.uk",
  "178.105.105.59",
  "localhost",
  "127.0.0.1",
]);

function normalizeHostname(hostname: string): string {
  return hostname.trim().toLowerCase().replace(/^https?:\/\//, "").replace(/\/.*$/, "");
}

export function isReservedHostname(hostname: string): boolean {
  const clean = normalizeHostname(hostname);

  if (RESERVED_HOSTNAMES.has(clean)) return true;

  // Block bare IPv4 addresses from being used as custom domains.
  if (/^(?:\d{1,3}\.){3}\d{1,3}$/.test(clean)) return true;

  return false;
}

export function assertPublishableHostname(hostname: string): { ok: true } | { ok: false; error: string } {
  const clean = normalizeHostname(hostname);

  if (isReservedHostname(clean)) {
    return {
      ok: false,
      error: `${clean} is a reserved control hostname and cannot be used as a project domain.`,
    };
  }

  return { ok: true };
}

// IP preview — per-project location blocks included into a shared server block
const NGINX_IP_PREVIEW_LOCATIONS = "/etc/nginx/prisom-project-locations";
const NGINX_IP_PREVIEW_CONF      = "/etc/nginx/sites-available/prisom-ip-preview.conf";
const NGINX_IP_PREVIEW_ENABLED   = "/etc/nginx/sites-enabled/prisom-ip-preview.conf";

export type RouteMode =
  | "fullstack_node"
  | "static_plus_api"
  | "static_only"
  | "api_only";

// ── Domain validation ──────────────────────────────────────────────────────

const DOMAIN_RE =
  /^[a-zA-Z0-9]([a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?(\.[a-zA-Z0-9]([a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?)*$/;

export function isValidDomain(hostname: string): boolean {
  if (!hostname || hostname.length < 4 || hostname.length > 253) return false;
  if (hostname.includes("..") || hostname.includes("/") || hostname.includes("\\"))
    return false;
  return DOMAIN_RE.test(hostname);
}

// ── Config generation ──────────────────────────────────────────────────────

/** Shared proxy location block snippet (WebSocket-compatible). */
function proxyLocation(prefix: string, port: number): string {
  return (
    `    location ${prefix} {\n` +
    `        proxy_pass         http://127.0.0.1:${port};\n` +
    `        proxy_http_version 1.1;\n` +
    `        proxy_set_header   Upgrade           $http_upgrade;\n` +
    `        proxy_set_header   Connection        'upgrade';\n` +
    `        proxy_set_header   Host              $host;\n` +
    `        proxy_set_header   X-Real-IP         $remote_addr;\n` +
    `        proxy_set_header   X-Forwarded-For   $proxy_add_x_forwarded_for;\n` +
    `        proxy_set_header   X-Forwarded-Proto $scheme;\n` +
    `        proxy_cache_bypass $http_upgrade;\n` +
    `    }\n`
  );
}

export interface NginxConfigOptions {
  hostname:        string;
  port:            number;
  routeMode?:      RouteMode;
  /** Absolute path to the static frontend files (for static_plus_api / static_only). */
  staticRoot?:     string;
  /** API prefix (default "/api") for static_plus_api mode. */
  apiPrefix?:      string;
}

/**
 * Generates a complete nginx HTTP server block for the given options.
 * Supports all four route modes.
 */
export function generateNginxConfig(opts: NginxConfigOptions): string {
  const { hostname, port } = opts;
  const mode      = opts.routeMode  ?? "fullstack_node";
  const apiPrefix = (opts.apiPrefix ?? "/api").replace(/\/*$/, "/"); // ensure trailing slash
  const staticRoot = opts.staticRoot ?? "";

  const header =
    `# Prisom project — ${hostname} [${mode}]\n` +
    `# Auto-generated by Prisom Project Panel. Manual edits will be overwritten.\n`;

  const serverOpen =
    `server {\n` +
    `    listen 80;\n` +
    `    server_name ${hostname};\n\n`;

  const serverClose = `}\n`;

  switch (mode) {
    case "fullstack_node":
    case "api_only":
      return header + serverOpen + proxyLocation("/", port) + serverClose;

    case "static_plus_api":
      if (!staticRoot) {
        // Fall back to fullstack if no static root provided
        return header + serverOpen + proxyLocation("/", port) + serverClose;
      }
      return (
        header +
        serverOpen +
        `    root ${staticRoot};\n` +
        `    index index.html;\n\n` +
        proxyLocation(apiPrefix, port) +
        `\n` +
        `    location / {\n` +
        `        try_files $uri $uri/ /index.html;\n` +
        `    }\n` +
        serverClose
      );

    case "static_only":
      if (!staticRoot) {
        return header + serverOpen + proxyLocation("/", port) + serverClose;
      }
      return (
        header +
        serverOpen +
        `    root ${staticRoot};\n` +
        `    index index.html;\n\n` +
        `    location / {\n` +
        `        try_files $uri $uri/ /index.html;\n` +
        `    }\n` +
        serverClose
      );
  }
}

