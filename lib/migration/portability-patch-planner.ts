/**
 * lib/migration/portability-patch-planner.ts
 *
 * Sprint 25: Generates patch plans for known Replit portability blockers.
 *
 * Supported patches:
 *   1. APP_URL replacement — creates a getPublicBaseUrl() helper
 *   2. Email transport replacement — creates SMTP transport + patches email file
 *
 * All patches are deterministic and safe:
 *   - Only writes under the project source dir
 *   - Never overwrites .env or lock files (enforced by safety layer)
 *   - "before" content is captured at plan-time for conflict detection at apply-time
 *   - Diffs are generated for UI preview
 */

